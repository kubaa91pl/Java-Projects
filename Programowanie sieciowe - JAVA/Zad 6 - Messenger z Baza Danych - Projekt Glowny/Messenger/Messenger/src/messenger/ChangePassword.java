    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String oldPassword = new String(jPasswordField1.getPassword());
        String newPassword1 = new String(jPasswordField2.getPassword());
        String newPassword2 = new String(jPasswordField3.getPassword());
        
        if(newPassword1.equals(newPassword2) == false){
            JOptionPane.showMessageDialog(
                    this,
                    "Proszę wprowadzić poprawnie nowe hasło",
                    "Błąd",
                    JOptionPane.ERROR_MESSAGE);
        }
        else{
            boolean success = DatabaseHelper.ChangePassword(oldPassword, newPassword1);
            
            if(success){
                JOptionPane.showMessageDialog(
                    this,
                    "Hasło zostało zmienione",
                    "Błąd",
                    JOptionPane.ERROR_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(
                    this,
                    "Zmiana hasła nie powiodła się",
                    "Błąd",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

